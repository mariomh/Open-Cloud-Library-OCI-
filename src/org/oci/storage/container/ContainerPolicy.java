/**
 * Stores informations on a container policy
 * @author mario_mh
 *
 */
public class ContainerPolicy {

	private String containerPolicyText;

	/**
	 * @return the containerPolicyText
	 */
	public String getContainerPolicyText() {
		return containerPolicyText;
	}

	/**
	 * @param containerPolicyText the containerPolicyText to set
	 */
	public void setContainerPolicyText(String containerPolicyText) {
		this.containerPolicyText = containerPolicyText;
	}
	
}
